const STORAGE_KEY = "askthevoid_queue";

function shuffle(arr: string[]): string[] {
  const a = [...arr];
  for (let i = a.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [a[i], a[j]] = [a[j], a[i]];
  }
  return a;
}

export function getTodayKey(): string {
  return new Date().toISOString().slice(0, 10);
}

export function getDailyQuestion(): { question: string; index: number; total: number } {
  const todayKey = getTodayKey();
  const raw = localStorage.getItem(STORAGE_KEY);
  let state: { queue: string[]; usedDates: string[] } = raw
    ? JSON.parse(raw)
    : { queue: shuffle(questions), usedDates: [] };

  // Already answered today
  if (state.usedDates.includes(todayKey)) {
    const idx = state.usedDates.indexOf(todayKey);
    const usedCount = state.usedDates.length;
    // figure out which question was shown today
    // It was the first in queue before today's pop — stored separately would be cleaner
    // We store the current question separately
    const currentRaw = localStorage.getItem(STORAGE_KEY + "_today");
    if (currentRaw) {
      const current = JSON.parse(currentRaw);
      return { question: current.question, index: current.index, total: questions.length };
    }
  }

  // Need a new question today
  if (state.queue.length === 0) {
    state.queue = shuffle(questions);
  }

  const question = state.queue.shift()!;
  const index = questions.length - state.queue.length;

  state.usedDates.push(todayKey);
  localStorage.setItem(STORAGE_KEY, JSON.stringify(state));
  localStorage.setItem(STORAGE_KEY + "_today", JSON.stringify({ question, index }));

  return { question, index, total: questions.length };
}
